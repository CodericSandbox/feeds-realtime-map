async function postToFeeds(feedName, data) {
	for (var i = 0; i < data.length; i++) {
		const path = data[i];

		// Start sending points to Feed instance
		feeds.publish(feedName, {
			lat: path.lat,
			lng: path.lng,
		}).then(() => {
			//console.log("Item Published!");
		}).catch(err => {
			console.log(err.description)
		});

		await sleep(200);
	}
}


